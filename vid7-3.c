#include <stdio.h>
int main(){
    int a, b;
    printf("Put a = ");
    scanf("%d",&a);
    printf("Put b = ");
    scanf("%d",&b);
    printf("These are RELATIONAL OPERATORS");
    printf("\nThe value of a==b is - %d",a==b);
    printf("\nThe value of a!=b is - %d",a!=b);
    printf("\nThe value of a>=b is - %d",a>=b);    
    printf("\nThe value of a<=b is - %d",a<=b);
    printf("\nThe value of a>b is - %d",a>b);
    printf("\nThe value of a<b is - %d",a<b);    

    return 0;

}